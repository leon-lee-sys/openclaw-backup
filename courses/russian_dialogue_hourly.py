def get_hourly_dialogue(hour=None):
    """获取指定小时或当前小时的对话"""
    if hour is None:
        from datetime import datetime
        hour = datetime.now().hour
    
    if hour < 8:
        hour = 8
    elif hour > 20:
        hour = 20
    
    # 映射到库中的小时（8-19）
    mapped_hour = min(max(hour, 8), 19)
    
    return DIALOGUE_LIBRARY.get(mapped_hour, DIALOGUE_LIBRARY[8])

def format_dialogue(dialogue_info):
    """格式化对话为可读文本"""
    lines = []
    lines.append(f"🕐 **{dialogue_info['hour']}** — {dialogue_info['scenario']}")
    lines.append("")
    
    for i, (ru, cn) in enumerate(dialogue_info['dialogue'], 1):
        lines.append(f"{i}. 🇷🇺 {ru}")
        lines.append(f"   🇨🇳 {cn}")
    
    return "\n".join(lines)

def main():
    from datetime import datetime
    
    current_hour = datetime.now().hour
    dialogue_info = get_hourly_dialogue(current_hour)
    
    print("=" * 50)
    print(f"俄语沉浸式对话 - {dialogue_info['hour']}")
    print("=" * 50)
    print()
    print(format_dialogue(dialogue_info))
    print()
    print("=" * 50)
    print(f"📝 共 {len(dialogue_info['dialogue'])} 句对话")
    print("=" * 50)

if __name__ == "__main__":
    main()